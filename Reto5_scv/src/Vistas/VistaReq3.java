    private void mostrarDatos(){
        Conexion objConn = new Conexion();
        
        try {
            ResultSet resultado = objConn.consultarRegistros("select m.Nombre_Material, m.Importado, COUNT(*) No_Compras from MaterialConstruccion m join Compra c on m.ID_MaterialConstruccion = c.ID_MaterialConstruccion where m.Importado = 'Si' group by c.ID_MaterialConstruccion order by No_Compras asc;");
            
            while (resultado.next()) {
                Object[] usuarios = {resultado.getString("Nombre_Material"),
                resultado.getString("Importado"),
                resultado.getString("No_Compras")};
                
                modelo.addRow(usuarios);
            }
        } catch (Exception e) {
            System.out.println("Error en Vista 1 "+e);
        }
    }
